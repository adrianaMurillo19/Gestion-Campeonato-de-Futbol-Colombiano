    private void tablaRegistrosJMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaRegistrosJMouseClicked
        // Para poder dar clic en alguna fila
        tablaRegistrosJ.getSelectionModel().addListSelectionListener(e ->{
        
        if (!e.getValueIsAdjusting() && tablaRegistrosJ.getSelectedRow() != -1){
            int fila = tablaRegistrosJ.getSelectedRow();
            
            
            txtnombre1.setText(tablaRegistrosJ.getValueAt(fila, 1).toString());
            txtNombre2.setText(tablaRegistrosJ.getValueAt(fila, 2).toString());
            txtApellido1.setText(tablaRegistrosJ.getValueAt(fila, 3).toString());
            txtApellido2.setText(tablaRegistrosJ.getValueAt(fila, 4).toString());
            txtCedula.setText(tablaRegistrosJ.getValueAt(fila, 5).toString());
            txtFechaN.setText(tablaRegistrosJ.getValueAt(fila, 6).toString());
            txtContrato.setText(tablaRegistrosJ.getValueAt(fila, 7).toString());
            txtNumCamiseta.setText(tablaRegistrosJ.getValueAt(fila, 8).toString());
            //seleccionar en el comboBox el estado correcto
            int idEquipo = Integer.parseInt(tablaRegistrosJ.getValueAt(fila, 9).toString());
            for (int i = 0; i < comboBoxEq.getItemCount(); i++){
                Equipo equipo = comboBoxEq.getItemAt(i);
                if (equipo.getIdEquipo() == idEquipo){
                    comboBoxEq.setSelectedIndex(i);
                    break;
                }
            }
            txtIdJugador.setText(tablaRegistrosJ.getValueAt(fila, 0).toString());// ID para actualizar
        }});
    }//GEN-LAST:event_tablaRegistrosJMouseClicked

