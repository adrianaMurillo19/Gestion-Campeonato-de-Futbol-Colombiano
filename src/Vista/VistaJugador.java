    /*Permite cargar el comboBox con el id y nombre de los equipos
    */
    private void cargarPartidosComboBox(){
        
        try {
            equipoDAO = new EquipoDAO(conn);
            List<Equipo> equipos = equipoDAO.obtenerTodos();
            comboBoxEq.removeAllItems();
            
            for(Equipo equipo: equipos)
                comboBoxEq.addItem(equipo);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
       
    /*permite invocar las funciones del modelo para cargar los datos del jugador en la tabla
    */
    private void cargarTablaJugadores(){
        
        DefaultTableModel modelo = (DefaultTableModel) tablaRegistrosJ.getModel();
        tablaRegistrosJ.getTableHeader().setReorderingAllowed(false); //Para evitar que se puedan mover las columnas del Jtable
        modelo.setRowCount(0);
        
        JugadorDAO dao = new JugadorDAO(conn);
        List<Jugador> jugador = dao.obtenerJugadores();
        
        for(Jugador j : jugador){
            modelo.addRow(new Object[]{
             j.getId_jugador(),
             j.getNombreUno(),
             j.getNombreDos(),
             j.getApellidoUno(),
             j.getApellidoDos(),
             j.getCedula(),
             j.getFechaNacimiento(),
             j.getContrato(),
             j.getNumCamiseta(),
             j.getEquipo_idEquipo()
            });
        }
        
    }
    

