    private void tablaRegistrosEMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaRegistrosEMouseClicked
        // Detectar clic en una fila de la tabla de Equipos
        tablaRegistrosE.getSelectionModel().addListSelectionListener(e -> {

            if (!e.getValueIsAdjusting() && tablaRegistrosE.getSelectedRow() != -1) {

                int fila = tablaRegistrosE.getSelectedRow();

                // Llenar campos de texto
                txtNombre.setText(tablaRegistrosE.getValueAt(fila, 1).toString());

                // Seleccionar Director Técnico
                int idDT = Integer.parseInt(tablaRegistrosE.getValueAt(fila, 2).toString());
                for (int i = 0; i < comboBoxDirectorTecnico.getItemCount(); i++) {
                    DirectorTecnico dt = comboBoxDirectorTecnico.getItemAt(i);
                    if (dt.getId_director_tecnico() == idDT) {
                        comboBoxDirectorTecnico.setSelectedIndex(i);
                        break;
                    }
                }

                // Seleccionar Ciudad
                int idCiudad = Integer.parseInt(tablaRegistrosE.getValueAt(fila, 3).toString());
                for (int i = 0; i < comboBoxCiudad.getItemCount(); i++) {
                    Ciudad ciudad = comboBoxCiudad.getItemAt(i);
                    if (ciudad.getIdCiudad() == idCiudad) {
                        comboBoxCiudad.setSelectedIndex(i);
                        break;
                    }
                }

                // ID del equipo (para actualizar o eliminar)
                txtIdEquipo.setText(tablaRegistrosE.getValueAt(fila, 0).toString());
            }
        });
    }//GEN-LAST:event_tablaRegistrosEMouseClicked

