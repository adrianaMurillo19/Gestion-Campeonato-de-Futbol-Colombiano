    private void btnConsultarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarEActionPerformed
        // Botón de buscar (id)

        try {
            String idEquipoStr = txtBuscarId.getText().trim();

            if (idEquipoStr.isEmpty()) {
                JOptionPane.showMessageDialog(
                    this,
                    "Por favor, ingrese un ID válido para consultar.",
                    "Campo vacío",
                    JOptionPane.WARNING_MESSAGE
                );
                return;
            }

            int idEquipo = Integer.parseInt(idEquipoStr);

            // Crear instancia del DAO
            EquipoDAO equipoDAO = new EquipoDAO(conn);

            // Consultar el equipo por ID
            Equipo equipo = equipoDAO.obtenerPorId(idEquipo);

            if (equipo != null) { // Si se encontró el equipo, llenar los campos

                txtIdEquipo.setText(String.valueOf(equipo.getIdEquipo()));
                txtNombre.setText(equipo.getNombre());

                // Seleccionar Director Técnico en ComboBox
                for (int i = 0; i < comboBoxDirectorTecnico.getItemCount(); i++) {
                    DirectorTecnico dt = (DirectorTecnico) comboBoxDirectorTecnico.getItemAt(i);

                    if (dt.getId_director_tecnico() == equipo.getDirectorTecnico_idUsuario()) {
                        comboBoxDirectorTecnico.setSelectedItem(dt);
                        break;
                    }
                }

                // Seleccionar Ciudad en ComboBox
                for (int i = 0; i < comboBoxCiudad.getItemCount(); i++) {
                    Ciudad ciudad = (Ciudad) comboBoxCiudad.getItemAt(i);

                    if (ciudad.getIdCiudad() == equipo.getCiudad_idCiudad()) {
                        comboBoxCiudad.setSelectedItem(ciudad);
                        break;
                    }
                }

                JOptionPane.showMessageDialog(
                    this,
                    "Equipo encontrado.",
                    "Consulta exitosa",
                    JOptionPane.INFORMATION_MESSAGE
                );

            } else {
                limpiarCampos();
                JOptionPane.showMessageDialog(
                    this,
                    "No se encontró ningún Equipo con ID: " + idEquipo,
                    "Equipo no encontrado",
                    JOptionPane.WARNING_MESSAGE
                );
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                this,
                "El ID del equipo debe ser un número válido.",
                "Error de formato",
                JOptionPane.ERROR_MESSAGE
            );
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnConsultarEActionPerformed

    private void btnGuardarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarEActionPerformed
        try {
            // Obtener datos del formulario
            String nombre = txtNombre.getText().trim();
            DirectorTecnico dtSeleccionado = (DirectorTecnico) comboBoxDirectorTecnico.getSelectedItem();
            Ciudad ciudadSeleccionada = (Ciudad) comboBoxCiudad.getSelectedItem();

            // Validaciones simples
            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El nombre del equipo no puede estar vacío.");
                return;
            }
            if (dtSeleccionado == null) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un Director Técnico.");
                return;
            }
            if (ciudadSeleccionada == null) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una Ciudad.");
                return;
            }

            // Crear objeto Equipo
            Equipo equipo = new Equipo();
            equipo.setNombre(nombre);
            equipo.setDirectorTecnico_idUsuario(dtSeleccionado.getId_director_tecnico());
            equipo.setCiudad_idCiudad(ciudadSeleccionada.getIdCiudad());

            // Guardar en BD
            EquipoDAO equipoDAO = new EquipoDAO(conn);
            boolean exito = equipoDAO.registrarEquipo(equipo);

            if (exito) {
                JOptionPane.showMessageDialog(null, "Equipo registrado exitosamente.");
                cargarTablaEquipos();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "Error al registrar el equipo.");
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarEActionPerformed

    private void btnActualizarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarEActionPerformed
        // Boton para actualizar datos del partido
        String idStr = txtIdEquipo.getText();
        if(idStr.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un equipo de la tabla para actualizar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
        try {

            Equipo equipo = new Equipo();

            equipo.setIdEquipo(Integer.parseInt(txtIdEquipo.getText()));
            equipo.setNombre(txtNombre.getText());
            DirectorTecnico directorSeleccionado = (DirectorTecnico) comboBoxDirectorTecnico.getSelectedItem();
            equipo.setDirectorTecnico_idUsuario(directorSeleccionado.getId_director_tecnico());
            Ciudad ciudadSeleccionada = (Ciudad) comboBoxCiudad.getSelectedItem();
            equipo.setCiudad_idCiudad(ciudadSeleccionada.getIdCiudad());

            EquipoDAO equipoDAO = new EquipoDAO(conn);//conexion con la BD

            boolean actualizado = equipoDAO.actualizarEquipo(equipo);

            if(actualizado){
                JOptionPane.showMessageDialog(null, "Equipo actualizado exitosamente");
                cargarTablaEquipos();
                limpiarCampos();
            }else{
                JOptionPane.showMessageDialog(null, "Error al actualizar el equipo");
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"Error " + ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarEActionPerformed

    private void btnEliminarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarEActionPerformed
        // Boton para eliminar algun registro que seleccione
        int fila = tablaRegistrosE.getSelectedRow();

        if(fila == -1){
            JOptionPane.showMessageDialog(null, "Seleccione un equipo a eliminar");
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Esta seguro de eliminar este registro? ", "confirmar", JOptionPane.YES_NO_OPTION);
        int idEquipo = Integer.parseInt(tablaRegistrosE.getValueAt(fila, 0).toString());

        EquipoDAO equipoDAO = new EquipoDAO(conn);
        boolean eliminado = equipoDAO.eliminarEquipo(idEquipo);

        if(eliminado){
            JOptionPane.showMessageDialog(null, "Equipo eliminado correctamente");
            cargarTablaEquipos();//Actualiza la tabla
        }else{
            JOptionPane.showMessageDialog(null, "No se pudo eliminar el equipo");
        }
    }//GEN-LAST:event_btnEliminarEActionPerformed

