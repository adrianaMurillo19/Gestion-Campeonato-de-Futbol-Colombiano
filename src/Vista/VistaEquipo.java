    // Carga el comboBox con los Directores Técnicos
    private void cargarDirectoresTecnicosComboBox() {
        try {
            directorTecnicoDAO = new DirectorTecnicoDAO(conn);
            List<DirectorTecnico> lista = directorTecnicoDAO.obtenerTodosLosDirectoresTecnicos();

            comboBoxDirectorTecnico.removeAllItems();

            for (DirectorTecnico dt : lista) {
                comboBoxDirectorTecnico.addItem(dt);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    // Carga el comboBox con las Ciudades
    private void cargarCiudadesComboBox() {
        try {
            ciudadDAO = new CiudadDAO(conn);
            List<Ciudad> ciudades = ciudadDAO.obtenerTodasLasCiudades();

            comboBoxCiudad.removeAllItems();

            for (Ciudad c : ciudades) {
                comboBoxCiudad.addItem(c);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    // Permite cargar los datos de los equipos en la tabla
    private void cargarTablaEquipos() {

        DefaultTableModel modelo = (DefaultTableModel) tablaRegistrosE.getModel();
        tablaRegistrosE.getTableHeader().setReorderingAllowed(false);
        modelo.setRowCount(0);

        EquipoDAO dao = new EquipoDAO(conn);
        List<Equipo> equipos = dao.obtenerEquipos();

        for (Equipo eq : equipos) {
            modelo.addRow(new Object[]{
                eq.getIdEquipo(),
                eq.getNombre(),
                eq.getDirectorTecnico_idUsuario(),
                eq.getCiudad_idCiudad()
            });
        }
    }



