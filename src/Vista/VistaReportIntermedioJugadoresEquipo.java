    private void cargarEquiposComboBox(){
        
        try {
            equipoDAO = new EquipoDAO(conn);
            List<Equipo> equipos = equipoDAO.obtenerTodos();
            comboBoxEquipo.removeAllItems();
            
            for(Equipo e: equipos)
                comboBoxEquipo.addItem(e);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

