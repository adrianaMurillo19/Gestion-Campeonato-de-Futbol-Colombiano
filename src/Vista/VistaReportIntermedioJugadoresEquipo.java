    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // Boton para generar reporte
        Equipo equipoSeleccionado = (Equipo) comboBoxEquipo.getSelectedItem();
        
        try {
            int idEquipo = (equipoSeleccionado.getIdEquipo());

            // 1. Obtener la lista de datos desde el DAO
            EquipoDAO dao = new EquipoDAO(conn); 
            Equipo equipo = dao.obtenerPorId(idEquipo);
            List<Jugador> jugadores = dao.obtenerJugadoresPorEquipo(idEquipo);

            // 2. Generar el reporte
            ReporteIntermedioJugadores reporte = new ReporteIntermedioJugadores();
            reporte.generarReporteJugadores(jugadores, idEquipo, equipo.getNombre());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Seleccione un equipo v√°lido.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al generar el reporte de Jugadores del equipo.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGenerarActionPerformed

