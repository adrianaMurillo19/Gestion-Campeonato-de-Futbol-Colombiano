    private void btnGenerarReporEdadJSimpleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarReporEdadJSimpleActionPerformed
        // Boton para generar reporte
        try {
            int edad = Integer.parseInt(txtReporEdadJSimple.getText());
            JugadorDAO dao = new JugadorDAO(conn);
            List<Jugador> jugadores = dao.obtenerJugadoresMayoresA(edad);
            
            ReporteJugadoresMayores reporte = new ReporteJugadoresMayores();
            reporte.generarReporteJugadoresMayores(jugadores, edad);
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese una edad v√°lida_");
        }
        
        
    }//GEN-LAST:event_btnGenerarReporEdadJSimpleActionPerformed

